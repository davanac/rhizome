-- Add comment for documentation
COMMENT ON TABLE public.web3auth_users IS 'Stores Web3Auth user authentication data and wallet addresses';
COMMENT ON COLUMN public.web3auth_users.web3auth_id IS 'Unique identifier from Web3Auth (verifierId or email)';
COMMENT ON COLUMN public.web3auth_users.wallet_address IS 'Ethereum wallet address generated by Web3Auth';
COMMENT ON COLUMN public.web3auth_users.verifier IS 'OAuth provider used for authentication';
COMMENT ON COLUMN public.web3auth_users.type_of_login IS 'Type of login method used (google, email, etc.)';

-- Migration complete message
DO $$
BEGIN
    RAISE NOTICE 'Web3Auth migration completed successfully';
    RAISE NOTICE 'Old wallet table has been dropped';
    RAISE NOTICE 'New web3auth_users table has been created';
    RAISE NOTICE 'Profiles and project_participants tables have been updated';
END $$;
